    private void CursosjList2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CursosjList2MouseClicked
        if (!CursosjList2.getSelectedValue().equals(null)){
            String color;
            try {
                String item = String.valueOf(CursosjList2.getSelectedValue());
                int idCurso = Integer.valueOf(item.substring(item.indexOf("<")+1, item.indexOf(">")));
                Cursos c = new Cursos();
                c.obtenerCursoSegunIdCurso(idCurso);
                CursosjCheckBox1.setSelected(c.getActivo()==1?true:false);
                CursoscantidadCuotasTxt.setText(String.valueOf(c.getCantidad_cuotas()));
                CursoscapacidadAulaTxt.setText(String.valueOf(c.getCapacidad_aula()));
                CursoscargaHorariaHorasTxt.setText(c.getCarga_horaria().substring(0,2));
                CursoscargaHorariaMinutosTxt.setText(c.getCarga_horaria().substring(3,5));

                color = c.getColor_fondo().startsWith("#")?c.getColor_fondo():"#"+c.getColor_fondo();
                color = color.toUpperCase();
                CursoscolorFondoTxt.setText(color);
                CursoscolorFondoTxt.setBackground(Color.decode(color));

                color = c.getColor_fuente().startsWith("#")?c.getColor_fuente():"#"+c.getColor_fuente();
                color = color.toUpperCase();
                CursoscolorFuenteTxt.setText(color);
                CursoscolorFuenteTxt.setBackground(Color.decode(color));

                CursoscostoInscripcionTxt.setText(String.valueOf(c.getCosto_inscripcion()));
                CursoscuotaSugeridaTxt.setText(String.valueOf(c.getCuota_sugerida()));
                CursosduracionCursoMesesTxt.setText(String.valueOf(c.getDuracion()));
                jXDatePicker5.getEditor().setText(c.getFecha_fin());
                jXDatePicker4.getEditor().setText(c.getFecha_inicio());
                CursosgProfesorTxt.setText(String.valueOf(c.getG_profesor()));
                CursosCBNombreCurso.setSelectedItem((new CursosNombres()).obtenerNombreSegunId(c.getIdNombreCurso()));
                CursosCBCicloLectivo.setSelectedItem(c.getObservaciones());
                CursosrequisitosPrvTxt.setText(c.getRequisitos());
                CursostituloTxt.setText(c.getTitulo());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_CursosjList2MouseClicked

