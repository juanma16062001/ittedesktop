    private void armarLista(List<String> lista, JList<String> jList, JScrollPane jScrollPane) {
        jList.setModel(new javax.swing.AbstractListModel<String>() {
            public int getSize() {
                return lista.size();
            }

            public String getElementAt(int i) {
                return lista.get(i);
            }
        });
        jScrollPane.setViewportView(jList);
    }
    
    public void obtenerCursos() throws HeadlessException {
        Cursos cursos = new Cursos();
        String aC = CursosSelecAnioParaCursos.getSelection().getActionCommand();
        cursos.obtenerCursosSegunAnio(aC);
        List<String> lista = new ArrayList<String>();
        while (cursos.sig()) {
                String item = !aC.equals("Todos")?(cursos.getNombre()+" <"+cursos.getId_curso()+">"):(cursos.getNombre()+" ("+cursos.getObservaciones()+")"+" <"+cursos.getId_curso()+">");
                lista.add(item);
        }
        armarLista(lista,CursosjList2,jScrollPane3);
    }
    
    public void obtenerNombresCursosParaComboBox () {
        CursosNombres cursosNombres = new CursosNombres();
        int cantNombres = cursosNombres.obtenerCantidad(), i = 0;
        String nombresCursos [] = new String [cantNombres];
        cursosNombres.obtenerTodos();
        while (cursosNombres.sig()) {
                nombresCursos[i++] = cursosNombres.getNombre();
        }
        CursosCBNombreCurso.setModel(new javax.swing.DefaultComboBoxModel<String>(nombresCursos));
    }

