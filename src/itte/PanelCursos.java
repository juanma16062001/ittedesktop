    private void CursosguardarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CursosguardarBtnActionPerformed
        try{
            Cursos c = new Cursos();
            c.setActivo(CursosjCheckBox1.isSelected()?1:0);
            c.setCantidad_cuotas(Integer.valueOf(CursoscantidadCuotasTxt.getText()));
            c.setCapacidad_aula(Integer.valueOf(CursoscapacidadAulaTxt.getText()));
            c.setCarga_horaria(CursoscargaHorariaHorasTxt.getText()+":"+CursoscargaHorariaMinutosTxt.getText()+":00");
            c.setColor_fondo(CursoscolorFondoTxt.getText());
            c.setColor_fuente(CursoscolorFuenteTxt.getText());
            c.setCosto_inscripcion(Double.valueOf(CursoscostoInscripcionTxt.getText()));
            c.setCuota_sugerida(Double.valueOf(CursoscuotaSugeridaTxt.getText()));
            c.setDuracion(Integer.valueOf(CursosduracionCursoMesesTxt.getText()));
            c.setFecha_fin(jXDatePicker5.getEditor().getText());
            c.setFecha_inicio(jXDatePicker4.getEditor().getText());
            c.setG_profesor(Double.valueOf(CursosgProfesorTxt.getText()));
            c.setIdNombreCurso((new CursosNombres()).obtenerIdSegunNombre(String.valueOf(CursosCBNombreCurso.getSelectedItem())));
            c.setNombre(String.valueOf(CursosCBNombreCurso.getSelectedItem())+ " " +String.valueOf(CursosCBCicloLectivo.getSelectedItem()));
            c.setObservaciones(String.valueOf(CursosCBCicloLectivo.getSelectedItem()));
            c.setRequisitos(CursosrequisitosPrvTxt.getText());
            c.setTitulo(CursostituloTxt.getText());
            c.guardarComoNueva();
            if(CursosCheckAnexarHorarioACurso.isSelected()){
                Horarios h = new Horarios();
                h.setId_curso(c.getIdUltimoCurso());
                String dia = String.valueOf(CursosCBProfesores.getSelectedItem()),
                dH = CursosTxtHorarioDesdeHora.getText(),
                dM = CursosTxtHorarioDesdeMinutos.getText(),
                aH = CursosTxtHorarioHastaHora.getText(),
                aM = CursosTxtHorarioHastaMinutos.getText();
                h.setHorario(dia,dH,dM,aH,aM);
            }
            JOptionPane.showMessageDialog(null, "Curso guardado Correctamente");
        } catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Error, el curso no pudo ser guardado, revise que todos los campos hayan sido completados. "+ ex.getMessage());
        } catch (IllegalAccessException ex) {
            JOptionPane.showMessageDialog(null, "Error, el curso no pudo ser guardado, revise que todos los campos hayan sido completados. "+ ex.getMessage());
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(null, "Error, el curso no pudo ser guardado, revise que todos los campos hayan sido completados. "+ ex.getMessage());
        } catch (InvocationTargetException ex) {
            JOptionPane.showMessageDialog(null, "Error, el curso no pudo ser guardado, revise que todos los campos hayan sido completados. "+ ex.getMessage());
        }
    }//GEN-LAST:event_CursosguardarBtnActionPerformed

