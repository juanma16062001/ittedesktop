    private void jTabbProfesoresStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jTabbProfesoresStateChanged
        if (jTabbProfesores.getSelectedIndex() == 1) { //cuando se hace clic sobre reporte de profesores
                Profesores profesores = new Profesores();
                profesores.todos();
                while (profesores.sig()) {
                    String item = String.valueOf(profesores.getId_profesor()) + "-" + profesores.getNombre() + " " + profesores.getApellido();
                    profesoresReporteCbProfesor.addItem(item);
                }

        }
    }//GEN-LAST:event_jTabbProfesoresStateChanged


