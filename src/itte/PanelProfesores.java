    public void obtenerProfesores() throws HeadlessException {
        Profesores profesores = new Profesores();
        profesores.todos();
        List<String> lista = new ArrayList<String>();
        while (profesores.sig()) {
                String item = profesores.getNombre()+" "+profesores.getApellido();
                lista.add(item);
        }
        armarLista(lista,profesoresListBoxUltimosCargados,jScrollPane2);
    }
    
    private void armarLista(List<String> lista, JList<String> jList, JScrollPane jScrollPane) {
        jList.setModel(new javax.swing.AbstractListModel<String>() {
            public int getSize() {
                return lista.size();
            }

            public String getElementAt(int i) {
                return lista.get(i);
            }
        });
        jScrollPane.setViewportView(jList);
    }

